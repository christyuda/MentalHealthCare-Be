const mongoose = require('mongoose');

const progressNoteSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const ProgressNote = mongoose.model('ProgressNote', progressNoteSchema);

module.exports = ProgressNote;
